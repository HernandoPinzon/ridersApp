    private boolean login(String usuario, String password){
        
        String query = "SELECT user_id, user_name FROM usuarios WHERE user_name='"+usuario+"' AND user_password='"+password+"'";
        try{
            cn = con.getConection();
            st = cn.createStatement();
            rs = st.executeQuery(query);
            while(rs.next()){
                
                user_id = rs.getInt("user_id");
                username = rs.getString("user_name");
                
            }
            if (username==null) {
                return false;
            }
            
            //System.out.println("user_id="+user_id+" y username="+username);
            
            return true;
        }catch(SQLException e){
            System.out.println(e);
            return false;
        }
    }

