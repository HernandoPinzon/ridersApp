    private boolean singin(String usuario, String password){
        String query = "INSERT INTO `usuarios`(`user_name`, `user_password`) VALUES ('"+usuario+"','"+password+"')";
        try{
            cn = conexion.getConection();
            st = cn.createStatement();
            st.executeUpdate(query);
            
            JOptionPane.showMessageDialog(this, "Usuario="+usuario+" con id="+password+" Fue agregado");
            return true;
        }catch(SQLException e){
            System.out.println(e);
            return false;
        }
    }

